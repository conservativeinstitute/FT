	<h2>Mission and Independence</h2>
	<p>The Federalist Times is an editorially independent publication. No advertiser, political organization, donor, or government entity has any influence over our editorial decisions. We exist to inform readers, not to advance any external agenda.</p>

	<h2>Separation of News and Opinion</h2>
	<p>The Federalist Times maintains a strict firewall between its news operation and its opinion section. News articles are reported, sourced, and edited without reference to the editorial positions of the publication or the personal views of any editor. Opinion pieces are clearly labeled and represent only the views of their individual authors.</p>

	<h2>Sourcing Standards</h2>
	<p>We require a minimum of two independent sources for any factual claim that is not a matter of public record. Anonymous sources are used only when the information cannot be obtained on the record and the source's reliability has been verified by at least one editor. We identify the reason for anonymity whenever possible.</p>

	<h2>Headline Accuracy</h2>
	<p>Headlines must accurately reflect the content of the article. We do not use misleading or sensationalized headlines to drive clicks. If a headline requires qualification, the qualification must be present in the headline itself — not buried in the body text.</p>

	<h2>AI Usage Disclosure</h2>
	<p>The Federalist Times may use artificial intelligence tools to assist with research, data analysis, and editorial workflows. AI-generated content is always reviewed, verified, and edited by human journalists before publication. We do not publish AI-generated articles without substantial human editorial oversight.</p>

	<h2>Conflicts of Interest</h2>
	<p>Reporters and editors are required to disclose any financial interest, personal relationship, or organizational affiliation that could reasonably be perceived as a conflict of interest. No staff member may cover a topic in which they have a material financial stake.</p>

	<h2>Independence from Advertisers</h2>
	<p>Advertising revenue does not influence editorial decisions. Advertisers have no advance knowledge of editorial content, no ability to review or approve stories before publication, and no input into editorial direction. Any advertiser who attempts to condition a buy on editorial treatment is refused.</p>

	<h2>Corrections</h2>
	<p>When we make errors, we correct them promptly and transparently. See our full <a href="<?php echo esc_url( home_url( '/corrections/' ) ); ?>" style="color:var(--gold);text-decoration:underline;text-underline-offset:2px;">Corrections and Updates Policy</a> for details.</p>
